import pandas as pd

# 1. Load the raw data
file_path = 'clinical_data/healthcare_dataset.csv'
df = pd.read_csv(file_path)

# 2. Date parsing
df['Date of Admission'] = pd.to_datetime(df['Date of Admission'])
df['Discharge Date'] = pd.to_datetime(df['Discharge Date'])
df['length_of_stay'] = (df['Discharge Date'] - df['Date of Admission']).dt.days

# 3. Data validation and error isolation (filtering zero/negative billing rows)
invalid_billing_mask = df['Billing Amount'] <= 0
violations_df = df[invalid_billing_mask].copy()
clean_df = df[~invalid_billing_mask].copy()

# 4. Export files
clean_df.to_csv("clean_patient_cohort.csv", index=False)
violations_df.to_csv("flagged_billing_errors.csv", index=False)

# 5. Summary statistics and correlations
mean_los = clean_df['length_of_stay'].mean()
age_los_corr = clean_df['Age'].corr(clean_df['length_of_stay'])

print(f"Total rows processed: {len(df)}")
print(f"Clean records saved: {len(clean_df)}")
print(f"Quarantined billing errors saved: {len(violations_df)}")
print(f"Average Length of Stay: {mean_los:.2f} days")
print(f"Correlation between Age and Length of Stay: {age_los_corr:.4f}")

# Operational Group-By Aggregations
# Group clean cohort by Medical Condition and Admission Type to analyze resource strain and costs
hospital_operations_summary = clean_df.groupby(['Medical Condition', 'Admission Type']).agg(
    Patient_Count=('Age', 'count'),
    Mean_LOS=('length_of_stay', 'mean'),
    Mean_Billing=('Billing Amount', 'mean')
).reset_index()

# Sort by Mean LOS descending to highlight conditions
hospital_operations_summary = hospital_operations_summary.sort_values(by='Mean_LOS', ascending=False)

# Export the executive summary
hospital_operations_summary.to_csv("hospital_operations_summary.csv", index=False)

# Print a preview of the top resource-heavy categories
print("\n--- Top Operational Insights (Service Line Utilization) ---")
print(hospital_operations_summary.head(10).to_string(index=False))

